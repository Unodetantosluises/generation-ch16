public class operadoresIncrementales {
    public static void main(String[] args) {
        //pre incremento
        int i = 1;
        int j = ++i;
        System.out.println("i = " + i);
        System.out.println("j = " + j);
        //pos ncremento
        i = 2;
        j = i--;

        System.out.println("i = " + i);
        System.out.println("j = " + j);
    }
}
